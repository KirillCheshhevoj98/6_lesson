#include<cstdio>

int main()
{
	/*
	Напечатать таблицу соответствия между весом в фунтах и весом в килограммах для значений 1, 2, ..., 10 фунтов (1 фунт = 453 г).
	*/
	for (int i = 1; i <= 10; i++)
	{
		float kil = i * 0.453;
		printf("|%2d|%2.3lf|\n", i, kil);
	}
	getchar();
	return 0;
}